"""Los booleans pueden tener dos valores True y False, podemos crear boleans con el operador (==).
True y False deben comenzar siempre con mayusculas"""

open = True
close = False

print(open == close )
print(2 == 3)
print("hello" == "hello" )
print(10 == 10)

"""Los boleanos se crean al comparar valores. Python tiene una serie de operadores de comparacion o tambien llamados operadores relacionales"""

x = 10

#igual que:
print(x == 10)
#mayor que:
print(x > 5)
#menor que:
print(x < 5)
#mayor igual que:
print(x >= 10)
#menor o igual que:
print(x <= 10)
#no igual a:
print(x != 8)

"""Los valores boleanos pueden representarse con valores enteros 1 y 0 respectivamente con la funcion int()"""

x = (7>5)
print(int(x))

b = (8>10)
print(int(b))

