"""Las listas son usadas para almacenar articulos. Estas son creadas usando [] corchetes con comas separando los elementos"""

listnums = ["uno", "dos", "tres"]
intnums = [1,2,3,4,5,6]
print(listnums)
print(intnums)

"""si queremos acceder a un determinado elemento tenemos que usar su indice en corchetes"""

print(listnums[0])
print(listnums[1])
print(listnums[2])

